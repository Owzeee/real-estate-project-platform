export const SITE_LANG_COOKIE = "site_lang";

export const supportedLocales = ["fr", "en"] as const;

export type SiteLocale = (typeof supportedLocales)[number];

const localeLabels = {
  fr: "FR",
  en: "EN",
} as const;

export function isSiteLocale(value: string | null | undefined): value is SiteLocale {
  return value === "fr" || value === "en";
}

export function getLocaleLabel(locale: SiteLocale) {
  return localeLabels[locale];
}

export function getTranslations(locale: SiteLocale) {
  return locale === "fr" ? fr : en;
}

